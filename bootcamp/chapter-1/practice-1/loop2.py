# Iterating over a list.

print("List Iteration")
l = ["tuli", "is", "smart"]
for i in l :
    print(i)

# Iterating over a tuple

print("\nTuple Interation")
t = ("tuli", "is", "smart")
for i in t:
    print(i)

# Iterating over a string

print("\nString Iteration")
s = "tuli"
for i in s :
    print(i)

# Iterating over a dictionary

print("\nDictionary Iteration")
d = dict()
d['xyz'] = 123
d['abc'] = 345
for i in d :
    print("&s %d" &(i, d[i]))        